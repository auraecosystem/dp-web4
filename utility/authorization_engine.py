# ---------------------------------------------------
# POLICY BASE CLASS
# ---------------------------------------------------

class Policy(ABC):

    @abstractmethod
    def evaluate(
        self,
        user: User,
        permission: Permission,
        resource: Resource,
    ) -> Decision:
        pass


# ---------------------------------------------------
# ROLE POLICY
# ---------------------------------------------------

class RolePolicy(Policy):

    def __init__(self, roles: Dict[str, Role]):

        self.roles = roles

    def evaluate(
        self,
        user,
        permission,
        resource,
    ) -> Decision:

        for role_name in user.roles:

            role = self.roles.get(role_name)

            if role is None:
                continue

            if permission in role.permissions:

                return Decision.ALLOW

        return Decision.ABSTAIN


# ---------------------------------------------------
# ATTRIBUTE POLICY
# ---------------------------------------------------

class AttributePolicy(Policy):

    def evaluate(
        self,
        user,
        permission,
        resource,
    ) -> Decision:

        if resource.owner == user.id:

            return Decision.ALLOW

        if user.attributes.get("department") == resource.attributes.get("department"):

            return Decision.ALLOW

        return Decision.ABSTAIN


# ---------------------------------------------------
# DENY POLICY
# ---------------------------------------------------

class DenyInactiveUsersPolicy(Policy):

    def evaluate(
        self,
        user,
        permission,
        resource,
    ):

        if not user.active:

            return Decision.DENY

        return Decision.ABSTAIN


# ---------------------------------------------------
# AUTHORIZATION ENGINE
# ---------------------------------------------------

class AuthorizationEngine:

    def __init__(self):

        self.roles: Dict[str, Role] = {}

        self.users: Dict[str, User] = {}

        self.policies: List[Policy] = []

        self.audit_log: List[AuditEntry] = []

    def register_role(self, role: Role):

        self.roles[role.name] = role

    def register_user(self, user: User):

        self.users[user.id] = user

    def add_policy(self, policy: Policy):

        self.policies.append(policy)

    def authorize(
        self,
        user_id: str,
        permission: Permission,
        resource: Resource,
    ) -> bool:

        user = self.users[user_id]

        for policy in self.policies:

            result = policy.evaluate(
                user,
                permission,
                resource,
            )

            if result == Decision.DENY:

                self.audit_log.append(
                    AuditEntry(
                        timestamp=datetime.utcnow(),
                        user=user.username,
                        permission=permission.name,
                        resource=resource.id,
                        decision="DENY",
                        reason=policy.__class__.__name__,
                    )
                )

                return False

            if result == Decision.ALLOW:

                self.audit_log.append(
                    AuditEntry(
                        timestamp=datetime.utcnow(),
                        user=user.username,
                        permission=permission.name,
                        resource=resource.id,
                        decision="ALLOW",
                        reason=policy.__class__.__name__,
                    )
                )

                return True

        return False


