# ---------------------------------------------------
# ENUMS
# ---------------------------------------------------

class Decision(Enum):
    ALLOW = "allow"
    DENY = "deny"
    ABSTAIN = "abstain"


# ---------------------------------------------------
# PERMISSION
# ---------------------------------------------------

@dataclass(frozen=True)
class Permission:
    name: str
    description: str = ""


